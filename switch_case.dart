void main() {
  var grade = 'A';
  switch (grade) {
    case 'A':
      print("you are good");
      break;
    case 'B':
      print("you are ok");
      break;
    case 'C':
      print("you are bad");
      break;
    case 'F':
      print("you are fail");
      break;
    default:
      print("invalid");
  }
}
/*
switch case statements only applicable for int string variable
*/
