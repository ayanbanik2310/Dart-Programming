void main()
{

  // final keyword
  final cityname="sylhet";
  // cityname="habiganj";  // it will show error if we want to change the value
  print(cityname);

  final String countryName="bangladesh";
  print(countryName);

  // const keyword
  const PI= 3.14;
  const gravity=9.8;
  print(PI);
  print(gravity);

}
/*

final and const used for those values which we don't want to change  

*/
